'use strict';

const num1 = Number(prompt('Please enter the first number'));
const num2 = Number(prompt('Please enter the second number'));

console.log(`result: ${num1} + ${num2} = ${num1 + num2}`);
console.log(`result: ${num1} - ${num2} = ${num1 - num2}`);
console.log(`result: ${num1} * ${num2} = ${num1 * num2}`);
console.log(`result: ${num1} / ${num2} = ${num1 / num2}`);